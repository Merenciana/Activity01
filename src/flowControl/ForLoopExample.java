package flowControl;

public class ForLoopExample {
    public static void main(String args[]) {
        //outer loop
        for (int rows = 0; rows <= 3; rows++) {
            //inner loop to print the numbers
            for (int columns = 0; columns <= 6; columns++) {
                System.out.print(" " + 5);

            }
            System.out.println(" ");
        }
    }
}
